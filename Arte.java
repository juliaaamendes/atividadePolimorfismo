public abstract class Arte {
    private String nome;
    
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    private String pintura;
    public String getPintura() {
        return pintura;
    }
    public void setPintura(String pintura) {
        this.pintura = pintura;
    }
}